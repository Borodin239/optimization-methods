package lab01.optimizations;

public class Iteration {
    private double l, r;

    public Iteration(double l, double r) {
        this.l = l;
        this.r = r;
    }

    public double getL() {
        return l;
    }

    public void setL(double l) {
        this.l = l;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }
}
