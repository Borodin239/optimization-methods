package lab01.tools;

public class Triple {
    private final double x_1;
    private final double x_2;
    private final double x_3;

    public Triple(double x_1, double x_2, double x_3) {
        this.x_1 = x_1;
        this.x_2 = x_2;
        this.x_3 = x_3;
    }

    public double getX_1() {
        return x_1;
    }

    public double getX_2() {
        return x_2;
    }

    public double getX_3() {
        return x_3;
    }
}
